                <h2 id="help-sobre-nos">Quem Somos?</h2>
                <p class="help-text">
                    O conceito do HortiBook surgiu no ano de 2020, 
                    pensando diretamente em como você - Cultivador e/ou Feirante - poderia melhorar a qualidade
                    de seu negócio, e contribui diretamente em como seus clientes veem você e seu hortifrúti.
                    Nossa startup visa viabilizar a velocidade de produção de forma saudável, natural e eficiente. 
                    Se você está se perguntando "como isso é possível?", nós vamos te explicar.
                </p>

                <h2>Como o HortiBook pode te ajudar?</h2>
                <p class="help-text">
                    Nosso trabalho é possível através da união de dois ramos de atuação dentro dessa mesma área:
                </p>
                    <ul class="info-help">
                        <li>Produtores de qualquer tipo de verdura, fruta ou legumes.</li>
                        <li>Vendedores desse mesmo material.</li>
                    </ul>
                <p class="help-text">
                    Tudo que nós fazemos é viabilizar o contato entre essas duas partes, possibilitando a comunicação
                    e acesso a demais informações de mutuo interesse. Assim, você terá a possibilidade de conseguir aquilo que precisa,
                    sem gastar nenhum centavo por isso.
                    Exemplo: Um cultivador de maçãs fecha um "Contrato" com uma rede de supermercado. Nesse contrato, 
                    ambos entram em um acordo. Para cada kg de fruta, inapropriada para o consumo, que esse supermercado 
                    fornecer ao cultivador, o mesmo lhes fornecerá 10kg de maças apropriadas para o consumo.
                </p>
                <h2>É Seguro?</h2>
                <p class="help-text">
                    Nossa equipe trabalha com um modelo de contrato padrão. Isso faz com que todos os usuários tenham as mesmas
                    condições de "acordo", sendo possível alterar somente informações pertinentes ao negócio. Como por exemplo: 
                    Quantidades, Matéria Prima, Tempo e Qualidade. Além de outras informações individuais de cada tipo de usuário.
                    Por isso, você está totalmente assegurado de que se houver qualquer descumprimendo das clausulas do contrato,
                    a parte responsável pela violação será colocada a disposição da lei.
                    Quanto aos dados de sua empresa, podemos lhe garantir que estarão protegidos em nosso sistema. Afinal, não
                    é necessário o compartilhamento de muitos dados, apenas o documento do usuario - podendo ser PF ou PJ - e a
                    identificação do mesmo. Esses dados são solicitados apenas para verificação da existência do usuário.
                </p>
